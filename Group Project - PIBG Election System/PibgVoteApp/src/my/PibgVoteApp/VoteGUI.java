    private void display() {
        // Clear the text area
        jTextArea1.setText("");

        // Print the table header
        String header = String.format("%-5s %-15s %-5s %-25s %-5s %s\n", "No.", "Name", "Age", "Job", "Exp.", "Achievements");
        jTextArea1.append(header);
        jTextArea1.append("--------------------------------------------------------------------------------------------\n");

        // Print each object in the list, including the candidate number
        int candidateNum = 1;
        for (Candidate c : list) {
            String row = String.format("%-5d %-15s %-5d %-25s %-5d %s\n", candidateNum, c.getName(), c.getAge(), c.getJob(), c.getExperience(), c.getAchievements());
            jTextArea1.append(row);
            candidateNum++;
        }
    }

    private void insertCandidate() {
        for (Candidate c : list) {
            jComboBox1.addItem(c.getName());
        }
    }

