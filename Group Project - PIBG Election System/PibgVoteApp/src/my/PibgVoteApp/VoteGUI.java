    private void voteSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_voteSaveActionPerformed
        // TODO add your handling code here:
        int response = JOptionPane.showConfirmDialog(this, "Do you want to proceed with your vote?", "Confirm Vote", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

        if (response == JOptionPane.YES_OPTION) {

            String selectedName = (String) jComboBox1.getSelectedItem();
            for (Voter v : voterlist) {
                if (v.getName().toLowerCase().trim().equals(name.toLowerCase().trim()) && v.getIcNo().toLowerCase().trim().equals(icNo.toLowerCase().trim())) {
                    // Update the "voted" attribute of the voter object
                    v.setVoted(selectedName);

                    try ( BufferedWriter bw = new BufferedWriter(new FileWriter("voter.txt"))) {
                        for (Voter voter : voterlist) {
                            bw.write(voter.getName() + "," + voter.getIcNo() + "," + voter.getVoted());
                            bw.newLine();
                        }
                    } catch (IOException e) {
                        e.printStackTrace();
                    }
                }
            }
            Object[] options = {"OK"};
            JOptionPane.showOptionDialog(this, "You vote has been recorded!", "Vote Successful",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE,
                    null, options, options[0]);

            VoteGUI.closeVoteGUI();
            new MainMenuGUI().setVisible(true);

        } else if (response == JOptionPane.NO_OPTION) {
            JOptionPane.getRootFrame().dispose();
        }


    }//GEN-LAST:event_voteSaveActionPerformed

