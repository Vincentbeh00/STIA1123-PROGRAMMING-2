    private void UpdateConBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateConBtnActionPerformed
        // TODO add your handling code here:
        list.set(candidateIndex, new Candidate(nameUpdate.getText(), Integer.parseInt(ageUpdate.getText()), jobUpdate.getText(), Integer.parseInt(expUpdate.getText()), bestUpdate.getText()));

        FileWriter writer;
        try {
            writer = new FileWriter("Candidate.txt");
            for (Candidate c : list) {
                String candidateInfo = c.getName() + "," + c.getAge() + "," + c.getJob() + "," + c.getExperience() + "," + c.getAchievements() + "\n";
                writer.write(candidateInfo);
            }
            writer.close();
            Object[] options = {"OK"};
            JOptionPane.showOptionDialog(this, "The Candidate Information has been updated!", "Information Update",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE,
                    null, options, options[0]);
            CandUpdate.setVisible(false);
            new adminGUI().setVisible(true);

        } catch (IOException ex) {
            Logger.getLogger(CandidateReg.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_UpdateConBtnActionPerformed

