    private void ConBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConBtn1ActionPerformed
        // TODO add your handling code here:
        try {
            int index = Integer.parseInt(txtUpdate.getText()) - 1;
            if (index >= 0 && index < list.size()) {
                Candidate c = list.get(index);
                int candidateIndex = list.indexOf(c);
                setCandidateDetails(c, candidateIndex);
                UpdateIndex.setVisible(false);
                adminGUI.closeAdmin();
                CandUpdate.setVisible(true);
                CandUpdate.setSize(740, 430);
                CandUpdate.setLocationRelativeTo(null);
            } else {
                Object[] options = {"OK"};
                JOptionPane.showOptionDialog(this, "Candidate not found, Please enter again!", "Error",
                        JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE,
                        null, options, options[0]);

                txtUpdate.setText("");
            }
        } catch (NumberFormatException e) {

            Object[] options = {"OK"};
            JOptionPane.showOptionDialog(this, "Please enter a valid candidate number!", "Error",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE,
                    null, options, options[0]);

            txtUpdate.setText("");
        }


    }//GEN-LAST:event_ConBtn1ActionPerformed

