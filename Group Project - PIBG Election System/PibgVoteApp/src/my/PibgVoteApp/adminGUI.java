    private void displayResultsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayResultsBtnActionPerformed
        // TODO add your handling code here:
        // Create an array to store the vote counts for each candidate
        int[] voteCounts = new int[list.size()];

        // Iterate through the voter list and count the votes for each candidate
        for (Voter v : voterlist) {
            for (int i = 0; i < list.size(); i++) {
                if (v.getVoted().equals(list.get(i).getName())) {
                    voteCounts[i]++;
                }
            }
        }
        // Display the results in the txtOutput text area
        txtOutput.setText("Results:\n=======================\n");
        for (int i = 0; i < list.size(); i++) {
            txtOutput.append(list.get(i).getName() + ": " + voteCounts[i] + " votes\n");
        }
        txtOutput.setCaretPosition(0);
    }//GEN-LAST:event_displayResultsBtnActionPerformed

    private void resetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetBtnActionPerformed
        // TODO add your handling code here:
        int choice = JOptionPane.showConfirmDialog(null, "Do you want to reset all the information?", "Reset", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.YES_OPTION) {
            try {
                list.clear();
                FileWriter fw = new FileWriter("Candidate.txt", false);
                fw.write("");
                fw.close();
                FileWriter vw = new FileWriter("voter.txt", false);
                vw.write("");
                vw.close();
                voterlist.clear();
                txtOutput.setText("");

                JOptionPane.showMessageDialog(null, "Information reset successfully");
            } catch (IOException e) {
                e.printStackTrace();
            }
        } else if (choice == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(null, "Reset canceled");
        }
    }//GEN-LAST:event_resetBtnActionPerformed

